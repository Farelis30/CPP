#include <iostream>
using namespace std;

int main(){
    int a, b;
    a = 100;
    b = 50;

    cout << "nilai a = " << a << endl;
    cout << "nilai b = " << b << endl;
    cout << endl;


    cout << "nilai a + b = " << a + b << endl; // penjumlahan
    cout << "nilai a - b = " << a - b << endl; // pengurangan
    cout << "nilai a * b = " << a * b << endl; // perkalian
    cout << "nilai a / b = " << a / b << endl; // pembagian
    cout << "nilai a % b = " << a % b << endl; // modulus    
}